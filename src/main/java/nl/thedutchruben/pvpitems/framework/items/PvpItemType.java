package nl.thedutchruben.pvpitems.framework.items;

/**
 * The PvpItemType is a class to sort the {@link PvpItem} in groups
 * @author Ruben de Roos
 * @since 1.0
 * @version 1.0
 */
public enum PvpItemType {
    /**
     * An pvp item wil be used for item's for pvp like swords sticks and rots
     */
    PVP_ITEM,
    /**
     * An medic item will be used for healing players
     */
    MEDIC_ITEM,
    /**
     * An item that a player can throw like an tnt or a knife
     */
    THROW_ITEM
}
